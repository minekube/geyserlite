pub(crate) fn write_floodgate_key(workdir: &Path, opts: &Options) -> Result<()> {
    if opts.auth_type != AuthType::Floodgate {
        return Ok(());
    }
    if opts.floodgate_key.len() != 16 {
        return Err(crate::Error::InvalidFloodgateKey);
    }
    let path = workdir.join("key.bin");
    let mut f = OpenOptions::new()
        .create(true)
        .truncate(true)
        .write(true)
        .mode(0o600)
        .open(&path)?;
    f.write_all(&opts.floodgate_key)?;
    Ok(())
}

pub(crate) fn write_permissions_yml(workdir: &Path) -> Result<()> {
    let path = workdir.join("permissions.yml");
    std::fs::write(path, "default-permissions: []\n")?;
    Ok(())
}

/// Split `"host:port"` with defaults for missing pieces. Handles IPv6
/// in brackets. Port comes out as `i64` because that's the YAML int
/// type and lets `config_overrides` merge against the same shape
/// without type confusion.
pub(crate) fn split_host_port(s: &str, default_host: &str, default_port: i64) -> (String, i64) {
    if s.is_empty() {
        return (default_host.into(), default_port);
    }
    let (host, port_str) = split_host_port_str(s);
    let host = if host.is_empty() {
        default_host.to_string()
    } else {
        host
    };
    let port = port_str.parse::<i64>().unwrap_or(default_port);
    (host, port)
}

fn split_host_port_str(s: &str) -> (String, String) {
    if let Some(rest) = s.strip_prefix('[') {
        if let Some(end) = rest.find(']') {
            let host = &rest[..end];
            let after = &rest[end + 1..];
            let port = after.strip_prefix(':').unwrap_or("");
            return (host.into(), port.into());
        }
    }
    if let Some(idx) = s.rfind(':') {
        return (s[..idx].into(), s[idx + 1..].into());
    }
    (s.into(), String::new())
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::options::{AuthType, Motd};

    fn opts(listen: &str, upstream: &str) -> Options {
        Options {
            listen: listen.into(),
            upstream: upstream.into(),
            auth_type: AuthType::Floodgate,
            floodgate_key: vec![0u8; 16],
            motd: Motd {
                line1: "alpha".into(),
                line2: "beta".into(),
            },
            ..Default::default()
        }
    }

    #[test]
    fn typed_options_apply() {
        let cfg = build_config(&opts("10.0.0.1:25599", "192.168.1.5:25567")).unwrap();
        let bedrock = cfg.get("bedrock").unwrap().as_mapping().unwrap();
        assert_eq!(bedrock.get("address").unwrap().as_str(), Some("10.0.0.1"));
        assert_eq!(bedrock.get("port").unwrap().as_i64(), Some(25599));
        assert_eq!(bedrock.get("motd1").unwrap().as_str(), Some("alpha"));
        let java = cfg.get("java").unwrap().as_mapping().unwrap();
        assert_eq!(java.get("address").unwrap().as_str(), Some("192.168.1.5"));
        assert_eq!(java.get("auth-type").unwrap().as_str(), Some("floodgate"));
        assert_eq!(
            cfg.get("floodgate-key-file").unwrap().as_str(),
            Some("key.bin"),
        );
    }

    #[test]
    fn overrides_deep_merge() {
        let mut o = opts(":19132", "127.0.0.1:25565");
        let mut bedrock_overrides = Mapping::new();
        bedrock_overrides.insert("compression-level".into(), 9.into());
        o.config_overrides
            .insert("bedrock".into(), bedrock_overrides.into());
        o.config_overrides
            .insert("passthrough-motd".into(), true.into());
        o.config_overrides.insert("max-players".into(), 50.into());

        let cfg = build_config(&o).unwrap();
        let bedrock = cfg.get("bedrock").unwrap().as_mapping().unwrap();
        assert_eq!(bedrock.get("compression-level").unwrap().as_i64(), Some(9));
        // Sibling under `bedrock` survives partial override.
        assert!(bedrock.get("server-name").unwrap().as_str().is_some());
        assert_eq!(cfg.get("passthrough-motd").unwrap().as_bool(), Some(true));
        assert_eq!(cfg.get("max-players").unwrap().as_i64(), Some(50));
    }

    #[test]
    fn override_beats_typed_option() {
        let mut o = opts("10.0.0.1:19132", "127.0.0.1:25565");
        let mut bedrock_overrides = Mapping::new();
        bedrock_overrides.insert("port".into(), 19999.into());
        o.config_overrides
            .insert("bedrock".into(), bedrock_overrides.into());

        let cfg = build_config(&o).unwrap();
        let bedrock = cfg.get("bedrock").unwrap().as_mapping().unwrap();
        assert_eq!(bedrock.get("port").unwrap().as_i64(), Some(19999));
    }

    #[test]
    fn split_simple() {
        assert_eq!(
            split_host_port("127.0.0.1:25567", "0.0.0.0", 19132),
            ("127.0.0.1".into(), 25567)
        );
    }
    #[test]
    fn split_default_host() {
        assert_eq!(
            split_host_port(":19132", "0.0.0.0", 19132),
            ("0.0.0.0".into(), 19132)
        );
    }
    #[test]
    fn split_ipv6() {
        assert_eq!(
            split_host_port("[::1]:25567", "0.0.0.0", 19132),
            ("::1".into(), 25567)
        );
    }
    #[test]
    fn split_fly() {
        assert_eq!(
            split_host_port("fly-global-services:19132", "0.0.0.0", 19132),
            ("fly-global-services".into(), 19132)
        );
    }
}
