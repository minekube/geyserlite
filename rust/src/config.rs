//! Render Geyser's `config.yml` from [`crate::Options`].
//!
//! Flow: parse a baseline YAML → fold the typed [`Options`] knobs
//! ([`Options::listen`], [`Options::upstream`], [`Options::auth_type`],
//! [`Options::motd`]) onto it → deep-merge [`Options::config_overrides`]
//! last → marshal back to YAML. The typed knobs stay the easy path
//! while every Geyser config option remains reachable through
//! `config_overrides` for the long-tail of settings the typed surface
//! doesn't model.

use std::fs::OpenOptions;
use std::io::Write;
use std::os::unix::fs::OpenOptionsExt;
use std::path::Path;

use serde_yaml_ng::{Mapping, Value};

use crate::error::Result;
use crate::options::{AuthType, Options};

pub(crate) fn render_config(workdir: &Path, opts: &Options) -> Result<()> {
    let cfg = build_config(opts)?;
    let body =
        serde_yaml_ng::to_string(&cfg).map_err(|e| crate::Error::Io(std::io::Error::other(e)))?;

    let path = workdir.join("config.yml");
    let mut f = OpenOptions::new()
        .create(true)
        .truncate(true)
        .write(true)
        .mode(0o600)
        .open(&path)?;
    f.write_all(b"# Generated by go.minekube.com/geyserlite/rust.\n")?;
    f.write_all(body.as_bytes())?;
    Ok(())
}

/// Pure-data half of `render_config`. Exposed so tests can assert the
/// merge result without writing files.
pub(crate) fn build_config(opts: &Options) -> Result<Mapping> {
    let mut cfg: Mapping = serde_yaml_ng::from_str(BASELINE_CONFIG_YAML)
        .map_err(|e| crate::Error::Io(std::io::Error::other(e)))?;

    let (bedrock_addr, bedrock_port) = split_host_port(&opts.listen, "0.0.0.0", 19132);
    let (upstream_addr, upstream_port) = split_host_port(&opts.upstream, "127.0.0.1", 25565);
    let motd1 = if opts.motd.line1.is_empty() {
        "geyserlite".to_string()
    } else {
        opts.motd.line1.clone()
    };
    let motd2 = if opts.motd.line2.is_empty() {
        "cross-play".to_string()
    } else {
        opts.motd.line2.clone()
    };

    let mut typed = Mapping::new();
    typed.insert("bedrock".into(), {
        let mut m = Mapping::new();
        m.insert("address".into(), bedrock_addr.into());
        m.insert("port".into(), Value::from(bedrock_port));
        m.insert("motd1".into(), motd1.clone().into());
        m.insert("motd2".into(), motd2.into());
        m.insert("server-name".into(), motd1.into());
        m.into()
    });
    typed.insert("java".into(), {
        let mut m = Mapping::new();
        m.insert("address".into(), upstream_addr.into());
        m.insert("port".into(), Value::from(upstream_port));
        m.insert("auth-type".into(), opts.auth_type.as_str().into());
        m.insert(
            "use-proxy-protocol".into(),
            (opts.auth_type == AuthType::Floodgate).into(),
        );
        m.insert("forward-hostname".into(), true.into());
        m.into()
    });

    merge_into(&mut cfg, &typed);
    if opts.auth_type == AuthType::Floodgate {
        cfg.insert("floodgate-key-file".into(), "key.bin".into());
    }
    if !opts.config_overrides.is_empty() {
        merge_into(&mut cfg, &opts.config_overrides);
    }
    Ok(cfg)
}

/// Recursively merge `src` into `dst`. When both sides have a nested
/// mapping at the same key, recurse; otherwise `src` wins. Mirrors
/// the Go `mergeMap` and Gate's `mergeConfigMaps`.
fn merge_into(dst: &mut Mapping, src: &Mapping) {
    for (k, v) in src {
        match (dst.get_mut(k), v) {
            (Some(Value::Mapping(dst_m)), Value::Mapping(src_m)) => merge_into(dst_m, src_m),
            _ => {
                dst.insert(k.clone(), v.clone());
            }
        }
    }
}

