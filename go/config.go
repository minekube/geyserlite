package geyserlite

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"text/template"
)

// renderConfig writes a Geyser config.yml into workdir based on opts.
//
// Geyser standalone reads config.yml from cwd; the runner sets cwd to
// workdir before launch.
func renderConfig(workdir string, opts Options) error {
	listenAddr, listenPort := splitHostPort(opts.Listen, "0.0.0.0", "19132")
	upstreamAddr, upstreamPort := splitHostPort(opts.Upstream, "127.0.0.1", "25565")

	data := configData{
		BedrockAddr:  listenAddr,
		BedrockPort:  listenPort,
		MOTD1:        defaultStr(opts.MOTD.Line1, "geyserlite"),
		MOTD2:        defaultStr(opts.MOTD.Line2, "cross-play"),
		ServerName:   defaultStr(opts.MOTD.Line1, "geyserlite"),
		UpstreamAddr: upstreamAddr,
		UpstreamPort: upstreamPort,
		AuthType:     opts.AuthType.String(),
		Floodgate:    opts.AuthType == Floodgate,
	}
	var buf strings.Builder
	if err := configTmpl.Execute(&buf, data); err != nil {
		return fmt.Errorf("geyserlite: render config: %w", err)
	}
	path := filepath.Join(workdir, "config.yml")
	if err := os.WriteFile(path, []byte(buf.String()), 0o600); err != nil {
		return fmt.Errorf("geyserlite: write config.yml: %w", err)
	}
	return nil
}

type configData struct {
	BedrockAddr, BedrockPort   string
	UpstreamAddr, UpstreamPort string
	MOTD1, MOTD2               string
	ServerName                 string
	AuthType                   string
	Floodgate                  bool
}

