// splitHostPort splits "host:port"; missing pieces fall back to defaults.
func splitHostPort(s, defaultHost, defaultPort string) (string, string) {
	if s == "" {
		return defaultHost, defaultPort
	}
	// IPv6 in brackets: "[::1]:25567"
	if strings.HasPrefix(s, "[") {
		end := strings.Index(s, "]")
		if end < 0 {
			return defaultHost, defaultPort
		}
		host := s[1:end]
		rest := s[end+1:]
		if !strings.HasPrefix(rest, ":") {
			return host, defaultPort
		}
		return host, rest[1:]
	}
	idx := strings.LastIndex(s, ":")
	if idx < 0 {
		return s, defaultPort
	}
	host := s[:idx]
	if host == "" {
		host = defaultHost
	}
	return host, s[idx+1:]
}

func defaultStr(s, fallback string) string {
	if s == "" {
		return fallback
	}
	return s
}
